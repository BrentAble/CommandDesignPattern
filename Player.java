public class Player {
	
	public Player() {}
	
	public void jump() {
		System.out.println("The Hero jumps a great distance in a single bound!");
	}
	
	public void fire() {
		System.out.println("The hero fires his lazer eyes!");
	}
	
	public void runForward() {
		System.out.println("The hero charges forward bravely");
	}
	/**
	 * Following methods will be what actions our hero will take
	 */
}
